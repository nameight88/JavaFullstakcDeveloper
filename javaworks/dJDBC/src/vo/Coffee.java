package vo;

public class Coffee {
	int c_No;
	String c_Name;
	int c_Price;
	
	public int getC_No() {
		return c_No;
	}
	public void setC_No(int c_No) {
		this.c_No = c_No;
	}
	public String getC_Name() {
		return c_Name;
	}
	public void setC_Name(String c_Name) {
		this.c_Name = c_Name;
	}
	public int getC_Price() {
		return c_Price;
	}
	public void setC_Price(int c_Price) {
		this.c_Price = c_Price;
	}
	
	
	
	
	
}
