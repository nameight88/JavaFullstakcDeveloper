package e_mathod;
/*overloading
 * 여러개의 함수가 동일한 함수명 사용
 * 인자의 자료형과 갯수가 달라야한다.
 * 
 */

public class Ex07_오버로딩 {

	public static void main(String[] args) {
		System.out.println(1000);//int
		System.out.println(3.99);//double
		System.out.println('왕');//char
		System.out.println("안녕하세요");//String
//		
//		[주의] 인자 자료형이나 갯수가 달라야하고
//		인자가 똑같고
//		리턴형만 다른 것은 아님
//		
//		void test(0)
//		double test(1)
//		char test(3)
	}

	
}

